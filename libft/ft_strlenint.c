#include "libft.h"

size_t	ft_strlenint(const int *s)
{
	int ret;
	int	i;

	ret = 0;
	i = 0;
	while (s[i])
	{
		ret += ft_lclen(s[i]);
		i++;
	}
	return (ret);
}
