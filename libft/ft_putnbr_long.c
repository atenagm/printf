#include "libft.h"

void	ft_putnbr_long(long n)
{
	if (n == -9223372036854775807 - 1)
		ft_putstr("-9223372036854775808");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr_long(n / 10);
			ft_putnbr_long(n % 10);
		}
		else
			ft_putchar(n + 48);
	}
}
