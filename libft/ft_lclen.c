#include "libft.h"

int	ft_lclen(int c)
{
	int i;

	i = 1;
	while ((c >>= 1))
		i++;
	if (i > 7)
	{
		if (i > 11)
		{
			if (i > 16)
				return (4);
			else
				return (3);
		}
		return (2);
	}
	else
		return (1);
}
